package list;
import list.Student;
public class Node {
	
	public Student stud;
	public Node next;
	
	public Node(Student stud) {
		super();
		stud = stud;
		this.next = next;
	}

	public Student getStud() {
		return stud;
	}

	public void setStud(Student stud) {
		this.stud = stud;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}
	
	
	
	
	

}
